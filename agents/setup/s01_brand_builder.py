    def __init__(self):
        super().__init__(name="S01_BrandBuilder")
        self.brand_profile = self._load_brand_profile()
        default_path = "docs/brand_guideline.md"
        self.guideline_path = self.brand_profile.get("file_paths", {}).get("guideline_path", default_path)

    def _load_brand_profile(self) -> Dict[str, Any]:
        """Loads the brand profile JSON."""
        if hasattr(self, "brand") and getattr(self, "brand", None):
            return self.brand.brand_config or {}
        return {}

    def _verify_outputs(self, action: str):
        """Verifies that expected assets depend on the action."""
        missing = []
        brand_name = self.brand.slug
        
        if action == "generate_assets":
            css_path = f"outputs/{brand_name}/assets/brand_style.css"
            if not os.path.exists(css_path):
                missing.append(css_path)
                
        if action == "collect_data":
            # Check if YouTube raw data was created (if configured)
            if self.brand_profile.get("youtube_video_urls"):
                yt_dir = f"outputs/{brand_name}/raw_data"
                if not os.path.exists(yt_dir) or not any(f.endswith(".json") or f.endswith(".txt") for f in os.listdir(yt_dir)):
                     # This is a loose check, mainly to ensure directory isn't empty
                     pass 

        if missing:
             self.log_activity(f"CRITICAL WARNING: Missing expected outputs: {missing}")

    def run(self, input_data: Dict[str, Any]) -> Dict[str, Any]:
        """
        Manages the lifecycle of brand guidelines.
        """
        action = input_data.get("action", "create")
        
        result = {}
        # 如果沒有指定 action，但有品牌資料，則預設為 create
        if "core_values" in input_data:
            result = self._create_guideline(input_data)
        elif action == "create":
            result = self._create_guideline(input_data)
        elif action == "update":
            result = self._update_guideline(input_data)
        elif action == "sync":
            result = self._sync_to_db()
        elif action == "collect_data":
            result = self._collect_data()
        elif action == "generate_assets":
            result = self._generate_assets()
        else:
            result = {"status": "error", "message": f"Unknown action: {action}"}
            
        self._verify_outputs(action)
        return result

    def _create_guideline(self, data: Dict[str, Any]) -> Dict[str, Any]:
        """
        Creates the initial brand guideline.
        
        執行流程：
        1. ⚙️ Python: 準備輸入資料
        2. 🧠 ANTIGRAVITY_WORKFLOW: 執行 s01_brand_builder.md 生成指南內容
        2. 🧠 ANTIGRAVITY_WORKFLOW: 執行 s01_brand_builder.md 生成指南內容
        3. ⚙️ Python: 寫入檔案並同步至 LanceDB
        """
        self.log_activity("Creating brand guideline...")
        
        # ⚙️ Step 1: Python 準備資料
        brand_data = {
            "core_values": data.get('core_values', 'To be defined'),
            "tone_voice": data.get('tone_voice', 'Professional, Authoritative, yet Accessible'),
            "persona": data.get('persona', 'General Audience'),
            "dos_donts": data.get('dos_donts', '- Do: Be clear\n- Don\'t: Use jargon')
        }
        
        # 🧠 Step 2: ANTIGRAVITY_WORKFLOW 執行點
        # 檢查是否由 Antigravity 傳入已生成的內容
        if "generated_content" in data:
            self.log_activity("Using content generated by Antigravity Workflow.")
            content = data["generated_content"]
        else:
            # Fallback: 使用臨時模板
            self.log_activity("No external content provided. Using fallback template.")
            content = self._generate_guideline_fallback(brand_data)
        
        # ⚙️ Step 3: Python 寫入檔案
        try:
            os.makedirs(os.path.dirname(self.guideline_path), exist_ok=True)
            with open(self.guideline_path, "w") as f:
                f.write(content)
                
            self.log_activity(f"Brand guideline created at {self.guideline_path}")
            
            # Auto-sync after creation
            self._sync_to_db()
            
            return {"status": "success", "message": "Brand guideline created and synced."}
            
        except Exception as e:
            self.log_activity(f"Error creating guideline: {e}")
            return {"status": "error", "message": str(e)}

