    def _sync_to_db(self) -> Dict[str, Any]:
        """Syncs the brand guideline to the Style DB."""
        self.log_activity("Syncing brand guideline to Style DB...")
        
        if not os.path.exists(self.guideline_path):
            return {"status": "error", "message": "Brand guideline file not found."}
            
        with open(self.guideline_path, "r", encoding="utf-8") as f:
            content = f.read()
        
        # 將 Markdown 按 H2 標題切分
        sections = self._split_by_h2(content)
        
        # 同步至 LanceDB Style DB
        items = []
        for idx, section in enumerate(sections):
            section_id = f"brand_guideline_section_{idx}"
            items.append({
                "id": section_id,
                "document": section["content"],
                "metadata": {
                    "id": section_id,
                    "type": "guideline",
                    "section": section["title"],
                    "source": "brand_guideline.md",
                    "status": "publish",
                    "date": datetime.datetime.now().isoformat(),
                    "modified": datetime.datetime.now().isoformat()
                }
            })
            
        vector_db.upsert_style_batch(items)
        
        self.log_activity(f"Synced {len(sections)} sections to Style DB")
        
        return {
            "status": "success", 
            "message": f"Brand guideline synced to Style DB ({len(sections)} sections).",
            "sections_stored": len(sections)
        }
    
    def _split_by_h2(self, markdown: str) -> list:
        """
        將 Markdown 按 H2 標題切分成多個段落
        
        Returns:
            [{"title": "章節標題", "content": "完整內容"}, ...]
        """
        sections = []
        current_section = {"title": "Introduction", "content": ""}
        
        for line in markdown.split("\n"):
            if line.startswith("## "):
                # 遇到新的 H2，儲存當前段落
                if current_section["content"].strip():
                    sections.append(current_section)
                # 開始新段落
                current_section = {
                    "title": line.replace("## ", "").strip(),
                    "content": line + "\n"
                }
            else:
                current_section["content"] += line + "\n"
        
        # 儲存最後一個段落
        if current_section["content"].strip():
            sections.append(current_section)
        
        return sections

